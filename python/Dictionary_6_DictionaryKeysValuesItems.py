
# Example 6: Dictionary Keys, Values, and Items
# You can retrieve all keys, values, or key-value pairs using the keys(), values(), and items() methods, respectively.

gio_info = {'place': 'Dhaka', 'postal_code': 1219, 'country': 'BD', 'population': 70000000}      # Example dictionary

keys = gio_info.keys()                         # Retrieving keys, values, and items
values = gio_info.values()
items = gio_info.items()

# Printing keys, values, and items
print('Keys:', keys)
print('Values:', values)
print('Items:', items)
