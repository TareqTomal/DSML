
# Example 1: Creating a Tuple
# A tuple is a collection of items, similar to a list, but it is immutable (cannot be changed).
# Tuples are created using parentheses ().

choice_list = (853, 'Berlin', ('Hamburg', 90), 'Paris', (42, 57))                           # Creating a tuple with various data types

print('The choice_list created by Tuple:', choice_list)                               # Printing the created tuple

