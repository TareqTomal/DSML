
# Example 8: Checking Membership in a List
# You can use the 'in' keyword to check if an element exists in a list.


fruits = ['avocado', 'kiwi', 'cherry', 'mango', 'orange', 'papaya']         # Example list


if 'avocado' in fruits:                                                     # Checking for membership
    print('avocado is in the list')
else:
    print('avocado is not in the list')
