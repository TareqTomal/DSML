
# Example 6: Tuple Slicing
# Slicing allows you to extract a part of the tuple.
# It is done using a colon (:) to specify start, stop, and step indices.


# Example tuple
chocolete_list= ('white chocoletes', 'dark chocoletes', 'hazelnut chocoletes', 'nutella', 'Milk chocoletes', 'ruby chocoletes')                           

sliced_chocolete_list = chocolete_list[2:5]                        # Slicing the tuple from index 1 to 4


print('The Sliced chocolete list is:', sliced_chocolete_list)                # Printing the sliced tuple
