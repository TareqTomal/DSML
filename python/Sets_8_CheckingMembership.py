
# Example 8: Checking Membership in a Set
# You can use the 'in' keyword to check if an element exists in a set.


student_list = {'ruben', 'nina', 'mickey', 'naomi', 'ron', 'alexandro', 'daniel', 'timo'}                   # Example set


if 'timo' in student_list:                                                                                             # Checking for membership
    print('"timo" is in the set')
else:
    print('"timo" is not in the set')
