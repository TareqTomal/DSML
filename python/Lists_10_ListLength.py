
# Example 10: List Length
# The len() function is used to find the number of elements in a list.

items = ['pin', 'scale', 'iron', 'sodium', 'magnesium', 'calcium']                               # Example list

length_of_items = len(items)                              # Getting the length of the list

# Printing the length
print('The Length of the items is :', length_of_items)
