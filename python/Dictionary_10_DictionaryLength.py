
# Example 10: Dictionary Length
# The len() function is used to find the number of key-value pairs in a dictionary.

gio_info = {'place': 'Dhaka', 'postal_code': 1219, 'country': 'BD', 'population': 70000000}      # Example dictionary


dict_length = len(gio_info)                                                     # Getting the length of the dictionary

print('Length of Dictionary:', dict_length)                                     # Printing the length

