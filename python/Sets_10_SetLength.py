
# Example 10: Set Length
# The len() function is used to find the number of elements in a set.

# Example set

country_set = {'Finland', 'Sweden', 'Denmark', 'Czech Republic', 'Austria', 'Switzerland','Czech Republic','Portugal',
                'Luxemburg','Norway', 'France', 'Italy'}


# Getting the length of the set
set_length = len(country_set)

# Printing the length
print('Length of country set:', set_length)
