
# Example 6: List Slicing
# Slicing allows you to extract a part of the list.
# It is done using a colon (:) to specify start, stop, and step indices.

item_list = ['mango', 'avocado', 'pitch', 'water melon', 'banana', 'palm', 'dates']                          # Example list


sliced_list = item_list[2:5]                                # Slicing the list from index 2 to 5


print('Sliced List is :', sliced_list)                      # Printing the sliced list
