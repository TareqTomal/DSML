
# Example 10: Tuple Length
# The len() function is used to find the number of elements in a tuple.

items = ['pin', 'scale', 'iron', 'sodium', 'magnesium', 'calcium']                            # Example list

items_length = len(items)                                                                     # Getting the length of the tuple

print('The Length of the items:', items_length)                                               # Printing the length

