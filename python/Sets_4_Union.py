
# Example 4: Set Union
# The union() method returns a new set containing all unique elements from both sets.
# You can also use the | operator to perform the union operation.

# Example sets
fruits_set1 = {'avocado', 'kiwi', 'cherry', 'mango', 'orange', 'papaya'}
fruits_set2 = {'jack fruit', 'grapes', 'blue berry', 'respberry'}

# Performing union
union_set = fruits_set1.union(fruits_set2)

# Printing the union set
print('Union of the 2 fruits sets is :', union_set)
