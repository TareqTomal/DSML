
# Example 9: Sorting a List
# The sort() method sorts a list in ascending order.
# You can also sort in descending order by setting the reverse parameter to True.


list1 = ['Ruben', 'Matthias', 'Lina', 'Daniel', 'Timo', 'Benedikt']                        # Example list                       


list1.sort()                                # Sorting in ascending order
print('Sorted List (Ascending):', list1)

list1.sort(reverse=True)                    # Sorting in descending order
print('Sorted List (Descending):', list1)
