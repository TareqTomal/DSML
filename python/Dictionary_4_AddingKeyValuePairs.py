
# Example 4: Adding Key-Value Pairs to a Dictionary
# You can add new key-value pairs to a dictionary by assigning a value to a new key.

gio_info = {'place': 'Dhaka', 'postal_code': 1219, 'country': 'BD'}      # Example dictionary



gio_info['population'] = '70000000'                                              # Adding a new key-value pair

# Printing the updated dictionary
print('The Updated gio_info:', gio_info)
