
# Example 2: Accessing Tuple Elements
# Elements in a tuple are accessed using indices, similar to lists.
# Indexing starts from 0 (first element) and can also be negative (-1 for the last element).

flowers = ('Rose', 'Marrygold', 'Sunflower', 'daizy', 'Lili', 'orchid')                         # Example tuple

first_flower = flowers[0]                                   # Accessing elements using positive and negative indices
third__flower= flowers[2]
last_flower = flowers[-1]

print('The first flower is :', first_flower)                # Printing accessed elements
print('The third flower is :', third__flower)
print('The Last flower is:', last_flower)
