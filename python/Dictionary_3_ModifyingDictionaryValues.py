
# Example 3: Modifying Dictionary Values
# Dictionaries are mutable, meaning their values can be changed by assigning a new value to a key.


gio_info = {'place': 'Dhaka', 'postal_code': 1219, 'country': 'BD'}      # Example dictionary

gio_info['postal_code'] = 1200                                           # Modifying a value
gio_info['country'] = 'Bangladesh'

print('Modified gio information:', gio_info)                             # Printing the modified dictionary
